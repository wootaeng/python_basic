# 주석은 #을 사용

# - 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1,3,0,-1)
print(1,3,0,-1, sep='') # 자동으로 들어가는 공백문자 제거
# print(3)
# print(0)
# print(-1)

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# - 문자열 자료형
# "" or ''
print("파이썬 떼잉", end='') # 프린트가 끝나면 자동으로 줄바꿈이 되는데 이어서 출력시 사용
print('파이썬 떼잉2')
print('"개 짖는 소리 좀 안나게 해라" 라고 그는 외쳤다.')


# - 불린형 자료형
# True or False
print(True)
print(False)

